# Funci√≥n para crear un FST que transforma presente simple a presente continuo
def create_fst_present_to_continuous():
    fst = FST()

    # Agregar transiciones del presente simple al presente continuo usando el diccionario
    for present, continuous in verbs_dict_present_to_continuous.items():
        fst.add_transitions([(0, present, 1, [continuous])])

    fst.add_start_state(0)
    fst.add_final_state(1)

    return fst


