from pyformlang.fst import FST
import random

def transformate_synonyms():
    fst = FST()
    for word, synonyms in synonyms_dict.items():
        fst.add_transitions([(0, word, 1, [random.choice(synonyms)])])

    fst.add_start_state(0)
    fst.add_final_state(1)

    return fst

