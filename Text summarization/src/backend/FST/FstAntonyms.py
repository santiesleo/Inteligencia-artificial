from pyformlang.fst import FST
import random

def transformate_antonyms():
    fst = FST()
    for word, antonyms in antonyms_dict.items():
        fst.add_transitions([(0, word, 1, [random.choice(antonyms)])])

    fst.add_start_state(0)
    fst.add_final_state(1)

    return fst

